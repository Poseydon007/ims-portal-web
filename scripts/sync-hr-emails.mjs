/**
 * sync-hr-emails.mjs
 *
 * Pulls real emails (and optionally profile data) from the HR Service and
 * updates the matching ims_users row, matched on hrEmployeeUuid.
 *
 * Idempotent — safe to re-run as many times as needed.
 *
 * Usage:
 *   node scripts/sync-hr-emails.mjs              # live update
 *   node scripts/sync-hr-emails.mjs --dry-run    # print what would change, no writes
 *   node scripts/sync-hr-emails.mjs --profile    # also sync fullName / department / position
 *
 * Required env vars:
 *   DATABASE_URL      mysql://user:pass@host:port/dbname
 *   HR_SERVICE_URL    https://hr.trueast-irp.cloud
 *   HR_SERVICE_KEY    hr_live_xxxxxxxxxxxxxxxxxxxx
 */

import { createConnection } from "mysql2/promise";

// ── Args ──────────────────────────────────────────────────────────────────────
const args = process.argv.slice(2);
const DRY_RUN     = args.includes("--dry-run");
const SYNC_PROFILE = args.includes("--profile");  // also sync name/dept/position

if (DRY_RUN) console.log("🔍  DRY RUN — no writes will be made\n");

// ── Env ───────────────────────────────────────────────────────────────────────
const { DATABASE_URL, HR_SERVICE_URL, HR_SERVICE_KEY } = process.env;

if (!DATABASE_URL) { console.error("❌  DATABASE_URL is required"); process.exit(1); }
if (!HR_SERVICE_URL) { console.error("❌  HR_SERVICE_URL is required"); process.exit(1); }
if (!HR_SERVICE_KEY) { console.error("❌  HR_SERVICE_KEY is required"); process.exit(1); }

const HR_BASE = HR_SERVICE_URL.replace(/\/$/, "");

// ── HR fetch ─────────────────────────────────────────────────────────────────
async function hrFetch(path) {
  const res = await fetch(`${HR_BASE}${path}`, {
    headers: { Authorization: `Bearer ${HR_SERVICE_KEY}` },
  });
  if (!res.ok) throw new Error(`HR API ${path} → ${res.status} ${res.statusText}`);
  return res.json();
}

// ── Placeholder detector ──────────────────────────────────────────────────────
// Emails generated by the import script look like te-0042@trueast.sa
// Any email ending in @trueast.sa with no dots in the local part is a placeholder.
function isPlaceholder(email) {
  if (!email) return true;
  // e.g.  te-0042@trueast.sa  or  sub-003@trueast.sa
  return /@trueast\.sa$/i.test(email) && !email.split("@")[0].includes(".");
}

// ── DB connect ────────────────────────────────────────────────────────────────
const url = new URL(DATABASE_URL);
const db = await createConnection({
  host:     url.hostname,
  port:     parseInt(url.port || "3306"),
  user:     url.username,
  password: url.password,
  database: url.pathname.slice(1),
  ssl:      { rejectUnauthorized: false },
});

// ── Fetch all HR employees ────────────────────────────────────────────────────
console.log("📡  Fetching employees from HR Service…");
let employees;
try {
  employees = await hrFetch("/v1/employees");
} catch (err) {
  console.error("❌  HR fetch failed:", err.message);
  await db.end();
  process.exit(1);
}
console.log(`    ${employees.length} employees returned\n`);

// ── Fetch all IMS users that have an hrEmployeeUuid ──────────────────────────
const [imsRows] = await db.execute(
  "SELECT id, email, fullName, department, position, hrEmployeeUuid FROM ims_users WHERE hrEmployeeUuid IS NOT NULL"
);

// Build a lookup: uuid → IMS row
const imsMap = new Map();
for (const row of imsRows) {
  imsMap.set(row.hrEmployeeUuid, row);
}
console.log(`🗂   ${imsRows.length} IMS users linked to HR\n`);

// ── Stats ─────────────────────────────────────────────────────────────────────
let updated      = 0;
let skippedSame  = 0;
let skippedNoEmail = 0;
let notFound     = 0;
let errors       = 0;

// ── Main loop ─────────────────────────────────────────────────────────────────
for (const emp of employees) {
  const imsUser = imsMap.get(emp.uuid);

  if (!imsUser) {
    // HR employee exists but no IMS account linked — normal for new HR hires
    // or employees not yet imported. Don't warn — just count.
    notFound++;
    continue;
  }

  // Determine the real email to use
  const hrEmail = emp.companyEmail?.trim().toLowerCase() || null;

  // Build the update payload
  const updates = {};

  // ── Email ──
  if (hrEmail && !isPlaceholder(hrEmail)) {
    const currentEmail = imsUser.email?.toLowerCase();
    if (currentEmail !== hrEmail) {
      updates.email = hrEmail;
    }
  } else {
    // No real email in HR yet — leave IMS email as-is
    skippedNoEmail++;
  }

  // ── Profile fields (opt-in via --profile) ──
  if (SYNC_PROFILE) {
    if (emp.fullName && emp.fullName !== imsUser.fullName) {
      updates.fullName = emp.fullName;
    }
    if (emp.department !== undefined && emp.department !== imsUser.department) {
      updates.department = emp.department ?? null;
    }
    if (emp.jobTitle !== undefined && emp.jobTitle !== imsUser.position) {
      updates.position = emp.jobTitle ?? null;
    }
  }

  if (Object.keys(updates).length === 0) {
    skippedSame++;
    continue;
  }

  // Build description for logging
  const changes = Object.entries(updates)
    .map(([k, v]) => {
      const old = imsUser[k] ?? "(none)";
      return `${k}: "${old}" → "${v}"`;
    })
    .join(", ");

  console.log(`  ${DRY_RUN ? "[DRY]" : "✓"} #${imsUser.id} ${imsUser.fullName} — ${changes}`);

  if (!DRY_RUN) {
    try {
      const setClauses = Object.keys(updates).map(k => `\`${k}\` = ?`).join(", ");
      const values     = [...Object.values(updates), imsUser.id];
      await db.execute(
        `UPDATE ims_users SET ${setClauses}, updatedAt = NOW() WHERE id = ?`,
        values
      );
      updated++;
    } catch (err) {
      console.error(`    ⚠  Failed to update #${imsUser.id}:`, err.message);
      errors++;
    }
  } else {
    updated++; // count as "would update" for dry-run summary
  }
}

await db.end();

// ── Summary ───────────────────────────────────────────────────────────────────
console.log("\n─────────────────────────────────────────");
console.log(`${DRY_RUN ? "Would update" : "Updated"}  : ${updated}`);
console.log(`No change    : ${skippedSame}`);
console.log(`No real email: ${skippedNoEmail}  (placeholder still in HR)`);
console.log(`Not in IMS   : ${notFound}  (HR employee not yet imported)`);
if (errors) console.log(`Errors       : ${errors}`);
console.log("─────────────────────────────────────────\n");

if (DRY_RUN && updated > 0) {
  console.log("Run without --dry-run to apply the changes.");
}
